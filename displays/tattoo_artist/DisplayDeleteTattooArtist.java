    private void jButtonDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonDeleteActionPerformed
        
        int confirm = JOptionPane.showConfirmDialog(null, "Do you really wanna to delete this tattoo artist?", "", JOptionPane.YES_NO_OPTION);
        if (confirm == JOptionPane.YES_OPTION) {
            TattooArtist artist = (TattooArtist) jComboBoxTattooArtists.getSelectedItem();
            try {
                if (TattooArtistDAO.delete(artist.getId())) {
                    this.dispose();
                    JOptionPane.showMessageDialog(null, "Delete sucess!", "", JOptionPane.INFORMATION_MESSAGE);
                } else {
                    this.dispose();
                    JOptionPane.showMessageDialog(null, "Error on delete!", "", JOptionPane.ERROR_MESSAGE);
                }
            } catch (ClassNotFoundException ex) {
                Logger.getLogger(DisplayDeleteTattooArtist.class.getName()).log(Level.SEVERE, null, ex);
            } catch (SQLException ex) {
                Logger.getLogger(DisplayDeleteTattooArtist.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_jButtonDeleteActionPerformed

