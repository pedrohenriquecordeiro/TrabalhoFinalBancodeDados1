    public void fillJComboBox() {

        ArrayList<TattooArtist> artist;
        try {
            artist = TattooArtistDAO.consultAll();
            DefaultComboBoxModel defaultComboBoxModel = (DefaultComboBoxModel) jComboBoxTattooArtists.getModel();
            for (TattooArtist a : artist) {
                if (this.atualUser.getId() != a.getId()) {
                    defaultComboBoxModel.addElement(a);
                }
            }
            jComboBoxTattooArtists.setModel(defaultComboBoxModel);
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(DisplayDeleteTattooArtist.class.getName()).log(Level.SEVERE, null, ex);
        } catch (SQLException ex) {
            Logger.getLogger(DisplayDeleteTattooArtist.class.getName()).log(Level.SEVERE, null, ex);
        }

    }

